
public class TrianglePrinter {

    public static void main(String[] args) {
        TrianglePrinter printer = new TrianglePrinter();
        printer.printAsterisk();
    }

    public String printAsterisk() {
        String toPrint = "*";
        System.out.println(toPrint);
        return toPrint;
    }

}
